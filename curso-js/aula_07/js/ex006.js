/*
O JS possui vários tipos de operadores, essa aula vamos ver 2, os operadores aritméticos e os operadores de atribuição

ARITMÉTICOS
* Aritméticos são usados para fazer operações matemáticas. 
* Eles possuem uma ordem de precedência que nos ajuda a resolver as contas.
* Para mais regras, leia o PDF do canva

ATRIBUIÇÃO
* Um operador de atribuição atribui um valor ao operando à sua esquerda baseado no valor do operando à direita. ex: 3 + 2 = 5
* Nos operadores de atribuição, usamos o sinal de " = ", que indica o valor/resultado
* Dentro dos operadores de atribuição, temos atribuição simples, alto atribuição e simplificando a auto-atribuição

   ATRIBUIÇÃO SIMPLES
   * Vamos atribuir valores de forma simples:
    var n = 3
    n = n + 3 (Resumindo: n = 3 + 3. RESULTADO 6)
    n = n + 5 (Resumindo: n = 6 + 5. RESULTADO 11)
    n = n + 1 (Resumindo: n = 11 + 1. RESULTADO 12)


    AUTO-ATRIBUIÇÃO
    * Refere a atribuição que é feita com a própria variável " n ", vamos sempre limpar a variável para que ela receba um novo valor. 
    var n = 2
    n = n + 1 (Resumindo: n = 2 + 1. RESULTADO 3)
    n = n + 5 (Resumindo: n = 3 + 5. RESULTADO 8)
    n = n + 6 (Resumindo: n = 8 + 6. RESULTADO 14)


    SIMPLIFICANDO A AUTO-ATRIBUIÇÃO

    * Forma de encurtar a escrita da auto-atribuição
    n = n + 1 --------- n += 1
    n = n + 5 --------- n += 5
    n = n + 6 --------- n += 6


OPERADORES DE INCREMENTO
* Servem para encurtar o n += 4 e acrescentar mais um valor.
ex:
var n = 4;
n++; //resultado é 5

*/