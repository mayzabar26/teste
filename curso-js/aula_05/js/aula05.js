//Variáveis
/*
-- São espaços que armazenam dados.
-- Esses dados podem ser números, strings, objetos, valores boolean (true, false)... 
-- Os nomes das variáveis são chamadas de identificadores
*/

//exemplos de variáveis

let name = "Madelaine";
let age = 30;
let occupation = "actress";

//Para descobrir quais tipo de de dados eu tenho na minha variável, eu posso usar o comando typeof (tipo de)

typeof name; 
typeof age;
typeof occupation; 

typeof "Madelaine"; 
typeof 30;
typeof "actress"; 

typeof []; //Retorna object 
typeof {}; //Retorna object 
typeof function(){}; //Retorna function
typeof NaN; //Retorna number
typeof Infinity; //Retorna number
typeof null; //Retorna object
typeof undefined; //Retorna undefined